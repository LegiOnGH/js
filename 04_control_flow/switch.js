const day = "Tuesday";

switch (day) {
    case "Monday":
        console.log("It's Monday!");    
        break;
    case "Tuesday":
        console.log("It's Tuesday!");
        break;
    case "Wednesday":
        console.log("It's Wednesday!");
        break;
    case "Thursday":
        console.log("It's Thursday!");
        break;
    case "Friday":
        console.log("It's Friday!");
        break;
    default:
        console.log("It's the weekend!");
}

const quarter = 2;

switch (quarter) {
    case 1:
        console.log("It's the first quarter of the year.");
        break;
    case 2:
        console.log("It's the second quarter of the year.");
        break;
    case 3:
        console.log("It's the third quarter of the year.");
        break;
    case 4:
        console.log("It's the fourth quarter of the year.");
        break;
    default:
        console.log("Invalid quarter number. Please enter a number between 1 and 4.");
}   